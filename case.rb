fav_color = 'pink'

case fav_color
when 'red'
	puts "Red like fire!"
when'orange'
	puts "Orange like, well...an orange."
when 'yellow'
	puts "Yellow daffodils are so pretty in the spring!"
when 'green'
	puts "Have you been to the Emerald City in Oz?"
when 'blue'
	puts "Blue like the sky!"
when 'purple'
	puts "Purple plums are the tastiest."
else
	puts "Hmm, well I don't know what that color is!"
end

weather = 'foggy'

case weather
when 'sunny'
	puts "Don't forget your sunglasses!"
when 'cloudy'
	puts "Better bring an umbrella, just in case!"
when 'rainy'
	puts "Why not stay in and read a good book instead?"
when 'snowy'
	puts "Grab some hot chocolate and pick out a good show on Netflix. We're not moving for a while!"
when 'hot'
	puts "PLEASE put on the air conditioning!"
when 'windy'
	puts "You might blow away if you go outside!"
else
	puts "Careful driving. Don't use your high beams!"
end